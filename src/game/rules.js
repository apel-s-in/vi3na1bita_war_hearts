/**
 * ПРАВИЛА ИГРЫ "ВОЙНА СЕРДЕЦ" (Классический Морской Бой 10х10)
 *
 * 1. Поле: квадрат 10×10.
 * 2. Флот:
 *    - 1×4 клетки;
 *    - 2×3 клетки;
 *    - 3×2 клетки;
 *    - 4×1 клетка.
 * 3. Расстановка:
 *    - корабли только горизонтально или вертикально;
 *    - диагонали, изгибы и зигзаги запрещены;
 *    - между любыми кораблями минимум 1 пустая клетка по сторонам и диагоналям;
 *    - корабли могут касаться краёв поля.
 * 4. Ходы:
 *    - ход переходит сопернику только при промахе;
 *    - при попадании, ранении или уничтожении корабля стрелявший продолжает ход.
 * 5. Выстрел:
 *    - каждый выстрел игрока подтверждается модалкой;
 *    - координата показывается как буква+цифра, например Д5;
 *    - выбранная клетка подсвечивается прицелом и пунктирными линиями.
 * 6. Уничтоженный корабль:
 *    - после полного уничтожения вокруг корабля отмечаются гарантированно пустые клетки;
 *    - эти клетки неактивны для выстрелов;
 *    - реальные выстрелы не затираются автоматической разметкой.
 */

export const GAME_RULES = {
  id: 'war_hearts_classic_10x10',
  boardSize: 10,
  // Конфиг: [размер_корабля, ...]
  ships: [4, 3, 3, 2, 2, 2, 1, 1, 1, 1],
  turnMode: 'classic',
  chat: true,
  voice: true
};

export const isInsideBoard = (x, y) =>
  Number.isInteger(x) && Number.isInteger(y) && x >= 0 && y >= 0 && x < GAME_RULES.boardSize && y < GAME_RULES.boardSize;
